package it.objectmethod.generaXml.model;

public class Anagrafica {

	//dati anagrafici
	private String nazione;
	private String par_iva;
	private String cod_fiscale;
	
	//anagrafica
	private String rag_soc_1;
	
	//sede
	private String indirizzo;
	private String cap;
	private String localita;
	private String provincia;
	
	
	
	
	
	public String getNazione() {
		return nazione;
	}
	public void setNazione(String nazione) {
		this.nazione = nazione;
	}
	public String getPar_iva() {
		return par_iva;
	}
	public void setPar_iva(String par_iva) {
		this.par_iva = par_iva;
	}
	public String getCod_fiscale() {
		return cod_fiscale;
	}
	public void setCod_fiscale(String cod_fiscale) {
		this.cod_fiscale = cod_fiscale;
	}
	public String getRag_soc_1() {
		return rag_soc_1;
	}
	public void setRag_soc_1(String rag_soc_1) {
		this.rag_soc_1 = rag_soc_1;
	}
	public String getIndirizzo() {
		return indirizzo;
	}
	public void setIndirizzo(String indirizzo) {
		this.indirizzo = indirizzo;
	}
	public String getCap() {
		return cap;
	}
	public void setCap(String cap) {
		this.cap = cap;
	}
	public String getLocalita() {
		return localita;
	}
	public void setLocalita(String localita) {
		this.localita = localita;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	
	
}
